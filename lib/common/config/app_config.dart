/// App configuration file
/// 
/// Force Update Configuration:
/// - Set [forceUpdateEnabled] to `true` to enable force update checks
/// - Set [forceUpdateEnabled] to `false` to disable force update checks
class AppConfig {
  /// Force update feature flag
  /// Change this value to enable/disable force update functionality
  /// 
  /// true = Force update is ENABLED (checks will run)
  /// false = Force update is DISABLED (checks will be skipped)
  static const bool forceUpdateEnabled = true;
}








